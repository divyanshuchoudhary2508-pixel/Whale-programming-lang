// genExpr returns (IR instructions to emit, the SSA register or literal
// holding the result, and the expression's Type).
func (g *Generator) genExpr(e Expr, out *strings.Builder) (string, string, Type) {
	switch ex := e.(type) {

	case *IntLit:
		return "", fmt.Sprintf("%d", ex.Value), TypeInt

	case *FloatLit:
		return "", fmt.Sprintf("%f", ex.Value), TypeFloat

	case *BoolLit:
		if ex.Value {
			return "", "1", TypeBool
		}
		return "", "0", TypeBool

	case *StringLit:
		globalName, length := g.internString(ex.Value)
		reg := g.newTemp()
		out.WriteString(fmt.Sprintf(
			"  %s = getelementptr inbounds [%d x i8], ptr %s, i64 0, i64 0\n",
			reg, length, globalName,
		))
		return "", reg, TypeString

	case *Ident:
		v, ok := g.scope[ex.Name]
		if !ok {
			panic(fmt.Sprintf("codegen: undefined variable %q", ex.Name))
		}
		reg := g.newTemp()
		out.WriteString(fmt.Sprintf("  %s = load %s, ptr %s\n", reg, g.llvmType(v.ty), v.llvmName))
		return "", reg, v.ty

	case *BinaryExpr:
		return g.genBinary(ex, out)

	case *CallExpr:
		return g.genCall(ex, out)

	case *StructLit:
		return g.genStructLit(ex, out)

	case *FieldAccess:
		return g.genFieldAccess(ex, out)

	case *ListLit:
		return g.genListLit(ex, out)

	case *IndexExpr:
		return g.genIndexExpr(ex, out)

	case *LenExpr:
		return g.genLenExpr(ex, out)

	case *AllocArray:
		return g.genAllocArray(ex, out)

	case *ConstructEnumExpr:
		return g.genConstructEnum(ex, out)

	case *MatchExpr:
		return g.genMatchExpr(ex, out)

	case *ChanRecvExpr:
		return g.genChanRecv(ex, out)

	default:
		panic(fmt.Sprintf("codegen: unhandled expression type %T", e))
	}
}

func (g *Generator) genBinary(ex *BinaryExpr, out *strings.Builder) (string, string, Type) {
	var body strings.Builder
	_, lReg, lTy := g.genExpr(ex.Left, &body)
	_, rReg, _ := g.genExpr(ex.Right, &body)

	reg := g.newTemp()
	op, isCmp := binaryOp(ex.Op, lTy)
	body.WriteString(fmt.Sprintf("  %s = %s %s %s, %s\n", reg, op, g.llvmType(lTy), lReg, rReg))

	if isCmp {
		return body.String(), reg, TypeBool
	}
	return body.String(), reg, lTy
}

// genCall handles function calls. print(...) is special-cased.
func (g *Generator) genCall(ex *CallExpr, out *strings.Builder) (string, string, Type) {
	if ex.Callee == "print" {
		return g.genPrintCall(ex, out)
	}

	var body strings.Builder
	argRegs := make([]string, len(ex.Args))
	argTypes := make([]Type, len(ex.Args))
	for i, a := range ex.Args {
		_, reg, ty := g.genExpr(a, &body)
		argRegs[i] = reg
		argTypes[i] = ty
	}

	argStrs := make([]string, len(ex.Args))
	for i := range ex.Args {
		argStrs[i] = fmt.Sprintf("%s %s", g.llvmType(argTypes[i]), argRegs[i])
	}

	calleeName := ex.Callee
	retTy := TypeInt // Default fallback for user functions

	switch ex.Callee {
	case "read_file": calleeName = "whale_read_file"; retTy = TypeString; g.declaredExterns[calleeName] = true
	case "write_file": calleeName = "whale_write_file"; retTy = TypeVoid; g.declaredExterns[calleeName] = true
	case "lines": calleeName = "whale_lines"; retTy = Type("[string]"); g.declaredExterns[calleeName] = true
	case "split": calleeName = "whale_split"; retTy = Type("[string]"); g.declaredExterns[calleeName] = true
	case "trim": calleeName = "whale_trim"; retTy = TypeString; g.declaredExterns[calleeName] = true
	case "replace": calleeName = "whale_replace"; retTy = TypeString; g.declaredExterns[calleeName] = true
	case "to_lower": calleeName = "whale_to_lower"; retTy = TypeString; g.declaredExterns[calleeName] = true
	case "to_upper": calleeName = "whale_to_upper"; retTy = TypeString; g.declaredExterns[calleeName] = true
	case "abs":
		if argTypes[0] == TypeFloat { calleeName = "whale_abs_f"; retTy = TypeFloat } else { calleeName = "whale_abs_i"; retTy = TypeInt }
		g.declaredExterns[calleeName] = true
	case "max":
		if argTypes[0] == TypeFloat { calleeName = "whale_max_f"; retTy = TypeFloat } else { calleeName = "whale_max_i"; retTy = TypeInt }
		g.declaredExterns[calleeName] = true
	case "min":
		if argTypes[0] == TypeFloat { calleeName = "whale_min_f"; retTy = TypeFloat } else { calleeName = "whale_min_i"; retTy = TypeInt }
		g.declaredExterns[calleeName] = true
	case "parse_int": calleeName = "whale_parse_int"; retTy = TypeInt; g.declaredExterns[calleeName] = true
	case "push": calleeName = "whale_push"; retTy = argTypes[0]; g.declaredExterns[calleeName] = true
	case "pop": calleeName = "whale_pop"; retTy = argTypes[0]; g.declaredExterns[calleeName] = true
	case "make_chan": calleeName = "whale_chan_new"; retTy = Type("chan"); g.declaredExterns[calleeName] = true
	}

	reg := g.newTemp()
	body.WriteString(fmt.Sprintf("  %s = call %s @%s(%s)\n", reg, g.llvmType(retTy), calleeName, strings.Join(argStrs, ", ")))
	return body.String(), reg, retTy
}

func (g *Generator) genChanRecv(ex *ChanRecvExpr, out *strings.Builder) (string, string, Type) {
	g.declaredExterns["whale_chan_recv"] = true
	var body strings.Builder
	_, chReg, _ := g.genExpr(ex.Chan, &body)
	
	valReg := g.newTemp()
	body.WriteString(fmt.Sprintf("  %s = call i64 @whale_chan_recv(ptr %s)\n", valReg, chReg))
	
	// Assume we are receiving int64 for MVP.
	return body.String(), valReg, TypeInt
}

// genPrintCall lowers print(x) to a call to libc's printf.
func (g *Generator) genPrintCall(ex *CallExpr, out *strings.Builder) (string, string, Type) {
	g.declaredExterns["printf"] = true
	var body strings.Builder
	
	var fmtStr strings.Builder
	argRegs := []string{}
	argTypes := []Type{}
	
	for _, arg := range ex.Args {
		_, argReg, argTy := g.genExpr(arg, &body)
		argRegs = append(argRegs, argReg)
		argTypes = append(argTypes, argTy)
		
		switch argTy {
		case TypeInt: fmtStr.WriteString("%ld")
		case TypeFloat: fmtStr.WriteString("%f")
		case TypeBool: fmtStr.WriteString("%d")
		case TypeString: fmtStr.WriteString("%s")
		default: panic("unsupported print type: " + argTy)
		}
	}
	fmtStr.WriteString("\n")
	
	fmtGlobal, fmtLen := g.internString(fmtStr.String())
	fmtPtrReg := g.newTemp()
	body.WriteString(fmt.Sprintf("  %s = getelementptr inbounds [%d x i8], ptr %s, i64 0, i64 0\n", fmtPtrReg, fmtLen, fmtGlobal))

	var callArgs strings.Builder
	callArgs.WriteString(fmt.Sprintf("ptr %s", fmtPtrReg))
	for i, reg := range argRegs {
		callArgs.WriteString(fmt.Sprintf(", %s %s", g.llvmType(argTypes[i]), reg))
	}
	
	resultReg := g.newTemp()
	body.WriteString(fmt.Sprintf("  %s = call i32 (ptr, ...) @printf(%s)\n", resultReg, callArgs.String()))
	return body.String(), resultReg, TypeInt
}

