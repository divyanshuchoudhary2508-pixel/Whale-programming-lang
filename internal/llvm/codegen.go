func (g *Generator) newTemp() string {
	name := fmt.Sprintf("%%t%d", g.tmpCount)
	g.tmpCount++
	return name
}

func (g *Generator) newLabel(prefix string) string {
	name := fmt.Sprintf("%s.%d", prefix, g.labelCount)
	g.labelCount++
	return name
}

// internString deduplicates string literals, returning the global constant
// name and the length (including null terminator, since LLVM needs it).
func (g *Generator) internString(s string) (string, int) {
	for _, sc := range g.stringConstants {
		if sc.value == s {
			return sc.globalName, sc.length
		}
	}
	name := fmt.Sprintf("@.str.%d", len(g.stringConstants))
	length := len(s) + 1 // +1 for the trailing null byte
	g.stringConstants = append(g.stringConstants, stringConst{
		globalName: name, value: s, length: length,
	})
	return name, length
}

func (g *Generator) exprType(e Expr) Type {
	switch ex := e.(type) {
	case *IntLit:
		return TypeInt
	case *FloatLit:
		return TypeFloat
	case *BoolLit:
		return TypeBool
	case *StringLit:
		return TypeString
	case *Ident:
		return g.scope[ex.Name].ty
	case *BinaryExpr:
		if isComparisonOp(ex.Op) {
			return TypeBool
		}
		return g.exprType(ex.Left)
	case *CallExpr:
		return TypeInt // see note in genCall about hardcoded return type
	case *StructLit:
		return Type(ex.StructName)
	case *ListLit:
		return Type("[" + string(ex.ElementType) + "]")
	case *IndexExpr:
		listTy := string(g.exprType(ex.List))
		if strings.HasPrefix(listTy, "[") && strings.HasSuffix(listTy, "]") {
			return Type(listTy[1 : len(listTy)-1])
		}
		return TypeInt
	case *LenExpr:
		return TypeInt
	case *AllocArray:
		return Type("[" + string(ex.ElementType) + "]")
	case *FieldAccess:
		objTy := g.exprType(ex.Object)
		structDecl, ok := g.structs[string(objTy)]
		if !ok {
			return TypeInt
		}
		for _, p := range structDecl.Fields {
			if p.Name == ex.Field {
				return p.Type
			}
		}
		return TypeInt
	default:
		panic(fmt.Sprintf("codegen: exprType: unhandled expression type %T", e))
	}
}

func (g *Generator) llvmType(t Type) string {
	switch t {
	case TypeInt:
		return "i64"
	case TypeFloat:
		return "double"
	case TypeBool:
		return "i1"
	case TypeString:
		return "ptr"
	case TypeVoid:
		return "void"
	default:
		if strings.HasPrefix(string(t), "[") || strings.HasPrefix(string(t), "list") {
			return "ptr" // %WhaleArray*
		}
		if _, ok := g.structs[string(t)]; ok {
			return "ptr" // structs are passed by reference
		}
		if _, ok := g.enums[string(t)]; ok {
			return "ptr" // enums are passed by reference
		}
		if strings.HasPrefix(string(t), "chan") {
			return "ptr" // channels are opaque pointers
		}
		panic(fmt.Sprintf("codegen: unhandled Type %v", t))
	}
}

